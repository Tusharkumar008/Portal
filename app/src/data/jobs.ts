export const getJobById = (id: string): Job | undefined => {
  return jobs.find(job => job.id === id);
};

export const getJobsByCategory = (category: string): Job[] => {
  return jobs.filter(job => job.category.toLowerCase() === category.toLowerCase());
};

export const getTrendingJobs = (limit: number = 6): Job[] => {
  return jobs.slice(0, limit);
};

export const getFeaturedJobs = (limit: number = 4): Job[] => {
  return jobs.filter(job => job.applicants && job.applicants > 30).slice(0, limit);
};
